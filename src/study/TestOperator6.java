package study;

//测试位运算符的用法（二进制）
public class TestOperator6 {

	public static void main(String[] arg) {
		String a = "3";
		int b = 4;
		System.out.println(a + b);

		char c1 = 'h';
		char c2 = 'i';
		System.out.println(c1 + c2);// char 相加是加法
		System.out.println("" + c1 + c2);// 空字符串加char，变成连接符

	}

}